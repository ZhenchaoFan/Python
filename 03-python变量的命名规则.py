# 变量名是由字母、数字、下划线组成，但是不能以数字开头
# 错误的便用  如： 3name = "李四"     name!age = 18
# 
# 变量命名的方式 ：  驼峰命名法 和 下划线命名法
# 驼峰命名法： 小驼峰和大驼峰

# 小驼峰： 第一个单词首字母要小写 其它单词首字线都大写
# 大驼峰： 每个单词首字母都大写
# 下划线命名法： 单词都使用小定字母，单词之间使用下划线进行分割，比如： my_name   _oldvan 这样子的都行

my_name = '李四'  # 下划线命名  ---推荐使用
_oldvan = '范爷' # 下划线命名
myName = '王麻'  # 小驼峰
MyName = '赵六' # 大驼峰

